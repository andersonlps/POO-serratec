package aula02;

public class ExemploVariaveis {
    public static void main(String[] args) {
        //Tipos primitivos - int, string, double Um tipo primitivo armazena valores
        //String, Double, Integer são classes
        String nome = "Ana";
        int idade = 20;
        float altura = 1.85f;
        double peso = 89.90;

        System.out.println("Nome :" + nome + "\nDados : " + idade + "\nAltura: " + altura + "\nPeso: " + peso);
        System.out.println(nome.toUpperCase());//Tudo em maiúsculo
    }
}
